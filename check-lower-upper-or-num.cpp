#include<iostream>
using namespace std;
int main(){
  int value;
  cout<<"***** this is a program to check whether a character is num,lower or uppercase *****"<<endl;
  cout<<"enter a character:- ";
  value=cin.get();
  cout<<"ascii value is "<<value<<endl;
  if(value>=48 && value<=57){
    cout<<"this is a numeric value"<<endl;
  }
  else if(value>=65 && value<=90){
    cout<<"this is an uppercase alphabet"<<endl;
  }
  else if(value>=97 && value<=122){
    cout<<"this is a lowercase alphabet"<<endl;
  }

  else{
    cout<<"none of them ";
  }
}
